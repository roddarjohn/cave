"""Benchmarks for ledger dimension operations.

Writes directly to the backing table generated by pgcraft's
``LedgerTablePlugin``.  The ledger has no private view — reads
go through a manually created balance aggregation view.
"""

import pytest
from sqlalchemy import Column, String, text

from tests.benchmarks.conftest import (
    SCHEMA,
    BenchLedger,
    build_metadata,
    create_all_from_metadata,
)

_TABLE = f"{SCHEMA}.bm_ledger"
_BALANCES = f"{SCHEMA}.bm_ledger_big_balances"


@pytest.fixture(scope="module")
def _ledger_tables(db_engine, bench_schema):  # noqa: ARG001
    meta, _ = build_metadata(
        BenchLedger,
        "bm_ledger",
        [Column("category", String)],
    )
    with db_engine.connect() as conn:
        create_all_from_metadata(conn, meta)


@pytest.fixture(scope="module")
def _seeded_ledger(db_engine, bench_schema):  # noqa: ARG001
    meta, _ = build_metadata(
        BenchLedger,
        "bm_ledger_big",
        [Column("category", String)],
    )
    with db_engine.connect() as conn:
        create_all_from_metadata(conn, meta)
        values = ", ".join(f"({i}, 'cat_{i % 5}')" for i in range(1, 10_001))
        conn.execute(
            text(
                f"INSERT INTO {SCHEMA}.bm_ledger_big"
                f" (value, category) VALUES {values}"
            )
        )
        conn.execute(
            text(
                f"CREATE OR REPLACE VIEW {_BALANCES} AS"
                f" SELECT category, SUM(value) AS balance"
                f" FROM {SCHEMA}.bm_ledger_big"
                f" GROUP BY category"
            )
        )
        conn.commit()


# -- single-row operations ------------------------------------------------


@pytest.mark.usefixtures("_ledger_tables")
class TestLedgerSingleRow:
    def test_insert_single(self, benchmark, bench_conn):
        counter = {"i": 0}

        def do_insert():
            counter["i"] += 1
            bench_conn.execute(
                text(
                    f"INSERT INTO {_TABLE}"
                    f" (value, category)"
                    f" VALUES ({counter['i']}, 'sales')"
                )
            )
            bench_conn.commit()

        benchmark.pedantic(do_insert, rounds=10_000)


# -- batch operations ------------------------------------------------------


@pytest.mark.usefixtures("_ledger_tables")
class TestLedgerBatch:
    def test_insert_batch_100(self, benchmark, bench_conn):
        counter = {"i": 0}

        def do_batch():
            counter["i"] += 1
            base = counter["i"] * 100
            values = ", ".join(
                f"({base + j}, 'cat_{j % 5}')" for j in range(100)
            )
            bench_conn.execute(
                text(f"INSERT INTO {_TABLE} (value, category) VALUES {values}")
            )
            bench_conn.commit()

        benchmark.pedantic(do_batch, rounds=100)

    def test_insert_batch_1000(self, benchmark, bench_conn):
        counter = {"i": 0}

        def do_batch():
            counter["i"] += 1
            base = counter["i"] * 1000
            values = ", ".join(
                f"({base + j}, 'cat_{j % 5}')" for j in range(1000)
            )
            bench_conn.execute(
                text(f"INSERT INTO {_TABLE} (value, category) VALUES {values}")
            )
            bench_conn.commit()

        benchmark.pedantic(do_batch, rounds=100)


# -- SELECT operations ----------------------------------------------------


@pytest.mark.usefixtures("_seeded_ledger")
class TestLedgerSelect:
    def test_select_balance(self, benchmark, bench_conn):
        def do_select():
            bench_conn.execute(text(f"SELECT * FROM {_BALANCES}")).fetchall()

        benchmark.pedantic(do_select, rounds=1_000)

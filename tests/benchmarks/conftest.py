# -- engine / connection ---------------------------------------------------


@pytest.fixture(scope="session")
def db_engine():
    """Session-scoped engine; skips if DATABASE_URL is unset."""
    url = os.environ.get("DATABASE_URL")
    if not url:
        pytest.skip("DATABASE_URL not set")
    return create_engine(url)


@pytest.fixture(scope="session")
def bench_schema(db_engine):
    """Create a dedicated schema for benchmarks; drop on teardown."""
    with db_engine.connect() as conn:
        conn.execute(text(f"DROP SCHEMA IF EXISTS {SCHEMA} CASCADE"))
        conn.execute(text(f"CREATE SCHEMA {SCHEMA}"))
        conn.commit()
    yield SCHEMA
    with db_engine.connect() as conn:
        conn.execute(text(f"DROP SCHEMA IF EXISTS {SCHEMA} CASCADE"))
        conn.commit()


@pytest.fixture
def bench_conn(db_engine, bench_schema):  # noqa: ARG001
    """Per-test connection that depends on bench_schema for setup."""
    with db_engine.connect() as conn:
        yield conn


# -- metadata → database --------------------------------------------------


def create_all_from_metadata(conn, metadata):
    """Create tables, views, functions, and triggers from *metadata*.

    Mirrors what Alembic does at migration time: tables via
    SQLAlchemy's ``create_all``, then views, functions, and triggers
    from the ``sqlalchemy_declarative_extensions`` registrations.
    """
    metadata.create_all(conn)

    funcs_obj = metadata.info.get("functions")
    if funcs_obj:
        for fn in funcs_obj.functions:
            schema = fn.schema or "public"
            conn.execute(
                text(
                    f"CREATE OR REPLACE FUNCTION"
                    f" {schema}.{fn.name}()"
                    f" RETURNS {fn.returns}"
                    f" LANGUAGE {fn.language}"
                    f" SECURITY DEFINER"
                    f" AS $$ {fn.definition} $$"
                )
            )

    views_obj = metadata.info.get("views")
    if views_obj:
        for view in views_obj:
            schema = view.schema or "public"
            defn = view.compile_definition()
            fqn = f"{schema}.{view.name}"
            conn.execute(text(f"CREATE OR REPLACE VIEW {fqn} AS {defn}"))

    triggers_obj = metadata.info.get("triggers")
    if triggers_obj:
        for trg in triggers_obj.triggers:
            conn.execute(text(trg.to_sql_create()))

    conn.commit()


def build_metadata(factory_cls, tablename, schema_items):
    """Build a pgcraft factory and return its metadata.

    Returns the ``(metadata, factory)`` tuple so callers can
    inspect context keys (e.g. the view name for SELECTs).
    """
    metadata = MetaData()
    factory = factory_cls(tablename, SCHEMA, metadata, schema_items)
    return metadata, factory

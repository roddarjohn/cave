"""Benchmarks for EAV dimension operations.

Uses the private pivot view (``schema.tablename``) generated by
pgcraft's ``EAVViewPlugin``.  INSTEAD OF triggers on the pivot
view handle INSERT and UPDATE.
"""

import pytest
from sqlalchemy import Column, String, text

from tests.benchmarks.conftest import (
    SCHEMA,
    BenchEAV,
    build_metadata,
    create_all_from_metadata,
)

_VIEW = f"{SCHEMA}.bm_eav"
_BIG_VIEW = f"{SCHEMA}.bm_eav_big"


@pytest.fixture(scope="module")
def _eav_tables(db_engine, bench_schema):  # noqa: ARG001
    meta, _ = build_metadata(
        BenchEAV,
        "bm_eav",
        [Column("sku", String), Column("color", String)],
    )
    with db_engine.connect() as conn:
        create_all_from_metadata(conn, meta)


@pytest.fixture(scope="module")
def _seeded_eav(db_engine, bench_schema):  # noqa: ARG001
    meta, _ = build_metadata(
        BenchEAV,
        "bm_eav_big",
        [Column("sku", String), Column("color", String)],
    )
    with db_engine.connect() as conn:
        create_all_from_metadata(conn, meta)
        values = ", ".join(f"('sku_{i}', 'color_{i}')" for i in range(10_000))
        conn.execute(
            text(f"INSERT INTO {_BIG_VIEW} (sku, color) VALUES {values}")
        )
        conn.commit()


# -- single-row operations ------------------------------------------------


@pytest.mark.usefixtures("_eav_tables")
class TestEAVSingleRow:
    def test_insert_single(self, benchmark, bench_conn):
        counter = {"i": 0}

        def do_insert():
            counter["i"] += 1
            bench_conn.execute(
                text(
                    f"INSERT INTO {_VIEW}"
                    f" (sku, color)"
                    f" VALUES"
                    f" ('SKU-{counter['i']}', 'red')"
                )
            )
            bench_conn.commit()

        benchmark.pedantic(do_insert, rounds=10_000)

    def test_update_single_attribute(self, benchmark, bench_conn):
        bench_conn.execute(
            text(f"INSERT INTO {_VIEW} (sku, color) VALUES ('UPD-SKU', 'blue')")
        )
        bench_conn.commit()
        row_id = bench_conn.execute(
            text(f"SELECT id FROM {_VIEW} WHERE sku = 'UPD-SKU' LIMIT 1")
        ).scalar()

        counter = {"i": 0}

        def do_update():
            counter["i"] += 1
            bench_conn.execute(
                text(
                    f"UPDATE {_VIEW}"
                    f" SET color = 'color_{counter['i']}'"
                    f" WHERE id = {row_id}"
                )
            )
            bench_conn.commit()

        benchmark.pedantic(do_update, rounds=10_000)


# -- batch operations ------------------------------------------------------


@pytest.mark.usefixtures("_eav_tables")
class TestEAVBatch:
    def test_insert_batch_100(self, benchmark, bench_conn):
        counter = {"i": 0}

        def do_batch():
            counter["i"] += 1
            base = counter["i"] * 100
            values = ", ".join(
                f"('SKU-B-{base + j}', 'clr_{base + j}')" for j in range(100)
            )
            bench_conn.execute(
                text(f"INSERT INTO {_VIEW} (sku, color) VALUES {values}")
            )
            bench_conn.commit()

        benchmark.pedantic(do_batch, rounds=100)


# -- SELECT operations ----------------------------------------------------


@pytest.mark.usefixtures("_seeded_eav")
class TestEAVSelect:
    def test_select_pivot(self, benchmark, bench_conn):
        def do_select():
            bench_conn.execute(text(f"SELECT * FROM {_BIG_VIEW}")).fetchall()

        benchmark.pedantic(do_select, rounds=1_000)

    def test_select_pivot_filtered(self, benchmark, bench_conn):
        def do_select():
            bench_conn.execute(
                text(f"SELECT * FROM {_BIG_VIEW} WHERE sku = 'sku_500'")
            ).fetchall()

        benchmark.pedantic(do_select, rounds=1_000)
